package jp.matsuda.Timer;

public class NoticeData {

	private String hour;
	private String min;
	private String sec;
	private int noticeAlarmId;
	private String noticeAlarmName;

	public String getHour() {
		return hour;
	}
	public void setHour(String hour) {
		this.hour = hour;
	}
	public String getMin() {
		return min;
	}
	public void setMin(String min) {
		this.min = min;
	}
	public String getSec() {
		return sec;
	}
	public void setSec(String sec) {
		this.sec = sec;
	}
	public int getNoticeAlarmId() {
		return noticeAlarmId;
	}
	public void setNoticeAlarmId(int nAlarmId) {
		this.noticeAlarmId = nAlarmId;
	}
	public String getNoticeAlarmName() {
		return noticeAlarmName;
	}
	public void setNoticeAlarmName(String nAlarmeName) {
		this.noticeAlarmName = nAlarmeName;
	}


}
